package kr.co.shop.vo;

public class SystemVO {
	 
    private String user_id; 
    private String logo;  
    private String system_nm;
    private String logo_img_change;  
    private String system_s_nm;
    private String modify_dt;
    private String create_dt;
    
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getSystem_nm() {
		return system_nm;
	}
	public void setSystem_nm(String system_nm) {
		this.system_nm = system_nm;
	}
	public String getLogo_img_change() {
		return logo_img_change;
	}
	public void setLogo_img_change(String logo_img_change) {
		this.logo_img_change = logo_img_change;
	}
	public String getSystem_s_nm() {
		return system_s_nm;
	}
	public void setSystem_s_nm(String system_s_nm) {
		this.system_s_nm = system_s_nm;
	}
	public String getModify_dt() {
		return modify_dt;
	}
	public void setModify_dt(String modify_dt) {
		this.modify_dt = modify_dt;
	}
	public String getCreate_dt() {
		return create_dt;
	}
	public void setCreate_dt(String create_dt) {
		this.create_dt = create_dt;
	}
}